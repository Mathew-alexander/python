print("Program to enter week number and print day of week")
a=int(input("Enter the week number: "))
if a==1:
    print("SUNDAY")
elif a==2:
    print("MONDAY")
elif a==3:
    print("TUESDAY")
elif a==4:
    print("WEDNESDAY")
elif a==5:
    print("THURSDAY")
elif a==6:
    print("FRIDAY")
elif a==7:
    print("SATURDAY")
else:
    print("IT IS NOT A WEEK NUMBER......")